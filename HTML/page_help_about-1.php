        <div class="pt-6">
            <h2 class="display">How does it work?</h2>
            <p class="text-base text-gray-500 mt-2 pr-4">Excellent question! When you upload a PNG (Portable Network Graphics) file, similar colors in your image are combined. This technique is called “quantization”. By reducing the number of colors, 24-bit PNG files can be converted to much smaller 8-bit indexed color images. All unnecessary metadata is stripped too. The result better PNG files with 100% support for transparency. Have your cake and eat it too!</p>
        </div>

        <div class="pt-6">
            <h2 class="display">Is it supported for all browsers?</h2>
            <p class="text-base text-gray-500 mt-2 pr-4">Excellent question! The files produced by Crushr are displayed perfectly on all modern browsers including mobile devices. Still need to support Internet Explorer 6? It normally ignores PNG transparency and displays a solid background color. With Crushr the background becomes transparent again. Binary transparency without any workarounds!</p>
        </div>

        <div class="pt-6">
            <h2 class="display">Why did you create Crushr?</h2>
            <p class="text-base text-gray-500 mt-2 pr-4">Excellent question! We frequently use PNG images, but were frustrated with the load times. We created Crushr in our quest to make our own websites faster and more fun to use with the best compression. In 2014 we added intelligent compression for JPEG images and in 2016 we added support for animated PNG. Compressing images with the website is free for everyone and we like to keep it that way! If you like Crushr please contribute by making a donation.</p>
